public class ArithmeticOperators {
    public static void main (String[] args) {
        
        //variables
        int num1, num2, res;
        num1 = 20;
        num2 = 5;

        //Addition
        res = num1 + num2;
        System.out.println("20 + 5 = " + res); // '+' is used for addition and concatenation

        //Subtraction
        res = num1 - num2;
        System.out.println("20 - 5 = " + res); // '+' is used for addition and concatenation

        //Multiplication
        res = num1 * num2;
        System.out.println("20 * 5 = " + res); // '+' is used for addition and concatenation

        //Division
        res = num1 / num2;
        System.out.println("20 / 5 = " + res); // '+' is used for addition and concatenation

        //Modulus
        res = num1 % num2;
        System.out.println("20 % 5 = " + res); // '+' is used for addition and concatenation
    }
}