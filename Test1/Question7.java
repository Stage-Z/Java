public class Question7 {
    public static void main(String[] args) {
        int a = 100;
        int b = 20;
        int res = 0;

        System.out.println("a = " + a);
        System.out.println("b = " + b + "\n");

        res = a + b;
        System.out.println("a + b = " + res + "\n This is the arithmetic operation of addition.");
        res = 0;

        res = a - b;
        System.out.println("a - b = " + res + "\n This is the arithmetic operation of subtraction.");
        res = 0;

        res = a * b;
        System.out.println("a * b = " + res + "\n This is the arithmetic operation of multiplication.");
        res = 0;

        res = a / b;
        System.out.println("a / b = " + res + "\n This is the arithmetic operation of division.");
        res = 0;
        
        res = a % b;
        System.out.println("a % b = " + res + "\n This is the arithmetic operation of modulus.");
        res = 0;
    }
}