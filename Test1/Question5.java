        System.out.println("Double: " + myDouble + "\n This is a double variable. It can store larger decimal numbers than float.\n");
        System.out.println("Character: " + myChar + "\n This is a character variable. It can store a single character.\n");
        System.out.println("Boolean: " + isLiving + "\n This is a boolean variable. It can store true or false values.\n");
        System.out.println("String: " + myString + "\n This is a string variable. It can store a sequence of characters or text.\n");
